      SUBROUTINE JD2000(DAY,YY,MM,DD,HH,MI,SEC)
CP GIVES THE NEW MOD. JULIAN DAY (MJD=0.0 ON 2000/JAN/1 AT 0:00:00)
CP FOR INPUT CALENDAR DATES BETWEEN 1950/JAN/1 AND 2099/DEC/31.
C
C   MJD(2000) = MJD(1950) - 18262.0 IS = 0 ON 2000/01/01 AT 00:00:00.
C
CI  (INT*4) YY = YEAR WITH 2 OR 4 DIGITS; 2 DIGITS => 1950 TO 2049
CI  (INT*4) MM = MONTH
CI  (INT*4) DD = DAY
CI  (INT*4) HH = HOUR
CI  (INT*4) MI = MINUTE
CI  (REAL) SEC = SECOND.
CO  (REAL) DAY = MOD. JUL. DAY, REFERRED TO 2000.
C
      IMPLICIT NONE
C
      REAL DAY0,DAY,SEC
      INTEGER YY, MM,DD,HH,MI,JJ,L,irc
C
      call iau_CAL2JD(yy,mm,dd,day0,day,irc)
      DAY = DAY + (DFLOAT((HH*60 + MI)*60) + SEC)/864.D2 ! add fraction of day
      day0=day0-2451544.5D0
      day=day0+day
C
C      write(*,*) 'JD2000:', yy,mm,dd,hh,mi,sec,day+2451544.5D0
C
      RETURN
      END
